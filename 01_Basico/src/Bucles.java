
public class Bucles {
	public static void main(String[] args) {
	
//--------------------------FOR---------------------------------------
        for(int i=0; i<=10; i++){
            System.out.println("El número es: "+i);
        }

//-------------------------WHILE--------------------------------------
        int contador=0;
        while(contador<=10){
            System.out.println("El contador vale: "+contador);
            contador++;
        }

//------------------------DO-WHILE-----------------------------------
        System.out.println(contador);
        contador=0;
        do{
            System.out.println("El contador vale: "+ contador); 
            contador++;
        }while(contador<=10);
        System.out.println(contador);
        
		
		
		
		
		
	}
}
